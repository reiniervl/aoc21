#[allow(dead_code)]
static TDATA: [u8; 12] = [
    0b00100, 0b11110, 0b10110, 0b10111, 0b10101, 0b01111, 0b00111, 0b11100, 0b10000, 0b11001,
    0b00010, 0b01010,
];

fn main() {
    test();
    let mut column: [u16; 12] = [0; 12];

    for i in 0..12 {
        let mut gamma: usize = 0;
        let mut epsilon: usize = 0;
        for n in DATA {
            let c1 = n & (0b100000000000 >> i);
            // println!("n: {}, c: {:b}", n, c1);
            if c1 == 0 {
                epsilon += 1;
            } else {
                gamma += 1;
            }
        }
        let c2 = if gamma > epsilon { 1 } else { 0 };
        column[i] = c2 << (11 - i);
        println!(
            "gamma: {}, epsilon: {}, column: {}",
            gamma, epsilon, column[i]
        );
    }

    let gamma: u16 = column.iter().sum();
    let epsilon: u16 = gamma ^ 0b111111111111;
    let gamma32: u32 = gamma.into();
    let epsilon32: u32 = epsilon.into();
    let result: u32 = gamma32 * epsilon32;
    println!("gamma:{}, epsilon:{}, result:{}", gamma, epsilon, result);
}

fn test() {
    let mut column: [u8; 12] = [0; 12];

    for i in 0..5 {
        let mut gamma: usize = 0;
        let mut epsilon: usize = 0;
        for n in TDATA {
            let c1 = n & (0b10000 >> i);
            // println!("n: {}, c: {:b}", n, c1);
            if c1 == 0 {
                epsilon += 1;
            } else {
                gamma += 1;
            }
        }
        let c2 = if gamma > epsilon { 1 } else { 0 };
        column[i] = c2 << (4 - i);
        println!(
            "gamma: {}, epsilon: {}, column: {}",
            gamma, epsilon, column[i]
        );
    }

    let gamma: u8 = column.iter().sum();
    let epsilon: u8 = gamma ^ 0b11111;
    println!(
        "gamma:{}, epsilon:{}, result:{}",
        gamma,
        epsilon,
        gamma * epsilon
    );
}

